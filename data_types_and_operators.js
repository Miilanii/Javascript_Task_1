// Exercise 2: Boolean and Operators

// Comparison Operators
let x = 8;
let y = 12;

let greaterThan = (x > y);
console.log("Is x greater than y?", greaterThan); 

let lessThanOrEqualTo = (x <= y);
console.log("Is x less than or equal to y?", lessThanOrEqualTo);

let equalTo = (x === y);
console.log("Is x equal to y?", equalTo); 

let notEqualTo = (x !== y);
console.log("Is x not equal to y?", notEqualTo);

// Logical Operators
let a = true;
let b = false;

let andOperator = (a && b);
console.log("a AND b", andOperator); 

let orOperator = (a || b);
console.log("a OR b", orOperator);

let notOperator = (!a);
console.log("NOT a", notOperator); 

